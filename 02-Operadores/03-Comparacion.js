// ><
let a = 10;

// * RELACIONALES
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// * IGUALDAD
console.log(a == 10); // true
console.log(a != 10); // false
console.log(a == '10'); // El contenido es igual (true), pero no está comparando el tipo de dato, tring, number, boolean, etc...

console.log(a === '10'); // Acá iguala todo, el contenido y el tipo.
console.log(a !== '10');