
let a = 5;
let b = 4;

console.log(a + b, 'suma'); // suna
console.log(a - b, 'resta'); // resta
console.log(a * b, 'multiplicación'); // multiplicación
console.log(a / b, 'division'); // división
console.log(a % b, 'módulo'); // módulo
console.log(a ** b, 'potencia'); // potencia


// incrementar
console.log(++a);
console.log(a++);
console.log(a);

// decrementar || disminuir
console.log(--b);
console.log(b--);
console.log(b);
