let animales = ['Perro', 'Gato', 'Pajaro']

// * EJEMPLO PARA VER LA LISTA UTILIZANDO 'FOR OF'
for (let animal of animales) {
    console.log(animal);
}

// * EJEMPLO PARA RECORRER LISTAS CON WHILE
let i = 0;
while (i < animales.length){
    console.log(animales[i]);
    i++
}